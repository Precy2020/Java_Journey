package deltel_deltel.chapter_5;

public class Triangle {
    public static void main(String[] args) {

   for(int love = 0; love < 9; love++){
       for(int peace = 0; peace < love; peace++){
       System.out.print("* ");
       }
       System.out.println();
   }
   for(int love = 1; love < 9; love++){
       for(int peace = 0; peace < love; peace++){
           System.out.print("* ");
       }
       System.out.println();
      }







    }
}
